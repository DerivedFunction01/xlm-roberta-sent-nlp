import pycountry

from script_types import Script
from source_config import LANGUAGE_BUCKETS

LANGUAGE_GROUPS = {group: cfg["langs"] for group, cfg in LANGUAGE_BUCKETS.items()}
LANGUAGE_GROUP_WEIGHTS = {group: float(cfg["weight"]) for group, cfg in LANGUAGE_BUCKETS.items()}
LANGUAGE_GROUP_MIN_CHARS = {group: int(cfg["min_chars"]) for group, cfg in LANGUAGE_BUCKETS.items()}
LATIN_GROUPS = {group for group, cfg in LANGUAGE_BUCKETS.items() if cfg.get("script") == Script.LATIN}
LANGUAGE_GROUP_SCRIPTS = {group: cfg["script"] for group, cfg in LANGUAGE_BUCKETS.items()}

LANGUAGE_ALIASES_JSON = Path(__file__).with_name("language_aliases.json")

def _load_language_aliases() -> dict[str, list[str]]:
    with LANGUAGE_ALIASES_JSON.open(encoding="utf-8") as f:
        raw = json.load(f)
    if not isinstance(raw, dict):
        raise ValueError(f"Expected JSON object in {LANGUAGE_ALIASES_JSON}")

    aliases: dict[str, list[str]] = {}
    for canonical, source_aliases in raw.items():
        if not isinstance(canonical, str) or not isinstance(source_aliases, list):
            continue
        aliases[canonical] = [str(alias) for alias in source_aliases if isinstance(alias, str) and alias]
    return aliases


LANGUAGE_ALIASES = _load_language_aliases()
ALL_LANGS = list(LANGUAGE_ALIASES.keys())
LANG_ISO2_TO_ISO3 = {
    lang: (
        getattr(pycountry.languages.get(alpha_2=lang) or pycountry.languages.get(alpha_3=lang), "alpha_3", None)
        or lang
    )
    for lang in ALL_LANGS
}
LANG_SOURCE_CODES = {
    lang: tuple(aliases)
    for lang, aliases in LANGUAGE_ALIASES.items()
}
LANG_SOURCE_TO_CANONICAL = {
    alias: canonical
    for canonical, aliases in LANG_SOURCE_CODES.items()
    for alias in aliases
}
for lang in ALL_LANGS:
    LANG_SOURCE_TO_CANONICAL.setdefault(lang, lang)

LANG_TO_GROUP = {lang: group for group, langs in LANGUAGE_GROUPS.items() for lang in langs}


def canonical_lang(lang: str) -> str:
    lang = (lang or "").strip().lower()
    return LANG_SOURCE_TO_CANONICAL.get(lang, lang)


def source_langs(lang: str) -> tuple[str, ...]:
    return LANG_SOURCE_CODES.get(canonical_lang(lang), ())


def preferred_source_lang(lang: str) -> str:
    aliases = source_langs(lang)
    return aliases[0] if aliases else canonical_lang(lang)


def dataset_label_script(label: str) -> Script | None:
    """Return the script suffix encoded in a dataset label like `arb_Latn`."""
    label = (label or "").strip()
    if "_" not in label:
        return None
    suffix = label.rsplit("_", 1)[1].strip()
    if not suffix:
        return None
    try:
        return Script(suffix)
    except ValueError:
        return None


def is_dataset_label_script_compatible(lang: str, label: str) -> bool:
    """Return False for Latin-script labels on non-Latin language groups."""
    label_script = dataset_label_script(label)
    if label_script is None:
        return True
    if label_script != Script.LATIN:
        return True
    return LANG_TO_GROUP.get(canonical_lang(lang)) in LATIN_GROUPS


